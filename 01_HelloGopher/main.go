// This is Basic Go format.
// package - import - main
// main function is not option!
// go compiler always see main function first.

package main // "main" is special in go.
// If you want to run golang script, package name should be main.

import "fmt" // This means format

/*
func main is also special
func main tells where to start!
*/
func main() {
	fmt.Println("Hello, Be a Gopher together!")
	// Upper case is for export/import in Go
	// Println means print line
}

// Gopher means golang developer.
// Welcome to the "GO world"
